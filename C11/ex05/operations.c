int	ft_add(int a, int b)
{
	return (a + b);
}

int	ft_minus(int a, int b)
{
	return (a - b);
}

int	ft_times(int a, int b)
{
	return (a * b);
}

int	ft_div(int a, int b)
{
	return (a / b);
}

int	ft_mod(int a, int b)
{
	return (a % b);
}
